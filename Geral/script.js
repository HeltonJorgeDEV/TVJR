const containerGeral = document.getElementById('lista-canais');
const catBar = document.getElementById('catBar');
const playerModal = new bootstrap.Modal(document.getElementById('playerModal'));
const iframe = document.getElementById('videoIframe');
const modalTitle = document.getElementById('modalTitle');

let filtroAtual = 'Todos';

function renderizarPagina() {
    containerGeral.innerHTML = '';
    
    for (const [categoria, canais] of Object.entries(categoriasCanais)) {
        if (filtroAtual !== 'Todos' && filtroAtual !== categoria) continue;

        const sectionHeader = document.createElement('div');
        sectionHeader.className = 'col-12';
        sectionHeader.innerHTML = `<h2 class="category-title">${categoria}</h2>`;
        containerGeral.appendChild(sectionHeader);

        canais.forEach(canal => {
            const col = document.createElement('div');
            col.className = 'col-6 col-md-4 col-lg-2 mb-4 canal-item';
            col.dataset.nome = canal.nome.toLowerCase();
            col.innerHTML = `
                <div class="card channel-card h-100 shadow">
                    <div class="card-body d-flex flex-column align-items-center justify-content-center p-2 text-center">
                        <div class="logo-wrapper-card">
                            <img src="${canal.logo}" alt="${canal.nome}" loading="lazy">
                        </div>
                        <span class="channel-name text-light">${canal.nome}</span>
                    </div>
                </div>
            `;
            col.onclick = () => {
                modalTitle.innerText = canal.nome;
                iframe.src = canal.url;
                playerModal.show();
            };
            containerGeral.appendChild(col);
        });
    }
}

function renderizarFiltros() {
    catBar.innerHTML = '';
    const categorias = ['Todos', ...Object.keys(categoriasCanais)];
    categorias.forEach(cat => {
        const btn = document.createElement('button');
        btn.className = `cat-btn ${filtroAtual === cat ? 'active' : ''}`;
        btn.innerText = cat;
        btn.onclick = () => {
            filtroAtual = cat;
            renderizarFiltros();
            renderizarPagina();
        };
        catBar.appendChild(btn);
    });
}

document.getElementById('inputBusca').addEventListener('input', (e) => {
    const termo = e.target.value.toLowerCase();
    document.querySelectorAll('.canal-item').forEach(item => {
        const visivel = item.dataset.nome.includes(termo);
        item.classList.toggle('d-none', !visivel);
    });
});

document.getElementById('playerModal').addEventListener('hidden.bs.modal', () => { iframe.src = ""; });

renderizarFiltros();
renderizarPagina();